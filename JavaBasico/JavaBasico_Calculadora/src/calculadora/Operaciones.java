package calculadora;

public class Operaciones {


    public static float Sumar(float a, float b) {

        return (a+b);
    }

    public static float Restar(float a, float b) {

        return (a-b);
    }

    public static float Dividir(float a, float b) {

        return (a/b);
    }

    public static float Resto(float a, float b) {

        return (a%b);
    }

}
